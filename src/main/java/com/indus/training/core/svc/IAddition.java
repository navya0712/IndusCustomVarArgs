package com.indus.training.core.svc;

/**
 * This interface defines a contract for performing addition operation.
 */
public interface IAddition {

	/**
	 * Performs addition of given array of double values
	 * 
	 * @param params an array of double values to be added
	 * @return the sum
	 */
	public double addition(double[] params);

}
